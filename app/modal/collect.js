let mongoose = require("mongoose");
let Schema = mongoose.Schema;
let CollectSchema = new Schema({
    collectid: String,
    fileid: String,
    userid: String,
	createTime: {
        type: Date,
        dafault: Date.now()
    },
	updateTime: {
        type: Date,
        dafault: Date.now()
    },
})

CollectSchema.pre('save', function(next) {
    if (this.isNew) {
      this.createTime = this.updateTime = Date.now()
    }
    else {
      this.updateTime = Date.now()
    }
    next()
})
class Collect{
    constructor() {
          this.collect = mongoose.model("collect", CollectSchema);
    }
    find(dataArr={}, skip={}) {
        const self = this;
        return new Promise(function (resolve, reject){
            self.collect.find(dataArr,null,skip, function(e, docs) {
                if(e){
                    console.log('e:',e);
                    reject(e);
                }else{
                    resolve(docs);
                }
            })
        })
    }
    create(dataArr) {
        const self = this;
        return new Promise(function (resolve, reject){
            let user = new self.collect({
                collectid: Date.parse(new Date()),
                fileid: dataArr.fileid,
                userid: dataArr.userid
            });
            user.save(function(e, data, numberAffected) {
                // if (e) response.send(e.message);
                if(e){
                    reject(e);
                }else{
                    resolve(data);
                }
            });
        })
    }
    update(dataArr){
        const self = this;
        return new Promise(function (resolve, reject){
            let arr = {
                collectid: Date.parse(new Date()),
                fileid: dataArr.fileid,
                userid: dataArr.userid
            };
            self.collect.update({
                _id: dataArr.id,
              }, arr,function(e, data, numberAffected) {
                // if (e) response.send(e.message);
                if(e){
                    reject(e);
                }else{
                    resolve(data);
                }
            });
        })
    }
    delete(dataArr) {
        const self = this;
        return new Promise(function (resolve, reject){
            self.collect.remove({
                _id: dataArr.id
            }, function(e, data) {
                if(e){
                    reject(e);
                }else{
                    resolve(data);
                }
            });
        })
    }
}

let collect = new Collect()
export {collect}
